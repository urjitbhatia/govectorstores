type testEmbedder struct{}

func (t testEmbedder) EmbedDocuments(ctx context.Context, texts []string) ([][]float32, error) {
	var vals [][]float32
	for _, doc := range texts {
		val, _ := t.EmbedQuery(ctx, doc)
		vals = append(vals, val)
	}
	return vals, nil
}

func (t testEmbedder) EmbedQuery(_ context.Context, text string) ([]float32, error) {
	return testEmbeddings[text], nil
}

var _ = Describe("pgvector store", Ordered, Label("pgvector"), func() {

	var store vectorstores.VectorStore
	bananaDoc := schema.Document{PageContent: "banana", Metadata: map[string]any{"foo": "bar"}}
	monkeyDoc := schema.Document{PageContent: "monkey", Metadata: nil}

	BeforeAll(func() {
		var err error
		store, err = pgvector.NewPGVectorStore(
			pgvector.WithCreds("test", "test"),
			pgvector.WithCollection("unittest"),
			pgvector.WithEndpoint("localhost", 5432),
			pgvector.WithDBName("test"),
			pgvector.WithEmbedder(testEmbedder{}),
		)
		Expect(err).ToNot(HaveOccurred())
		err = store.(*pgvector.Store).ClearCollection()
		Expect(err).ToNot(HaveOccurred())
	})

	It("stores documents", func() {
		Label("pgvector")
		docs := []schema.Document{bananaDoc, monkeyDoc}
		err := store.AddDocuments(context.Background(), docs)
		Expect(err).ToNot(HaveOccurred())
	})

	XIt("fetch documents by similarity", func() {
		ctx := context.WithValue(context.Background(), "dbtx", testTransaction)
		docs, err := store.SimilaritySearch(ctx, "bandana", 1)
		Expect(err).ToNot(HaveOccurred())

		Expect(len(docs)).To(Equal(1))
		Expect(docs[0].PageContent).To(Equal("banana"))
	})

	DescribeTable("fetch docs by similarity",
		func(query, expected string, expectedMeta map[string]any, scoreComparator string, scoreVal float32) {
			docs, err := store.SimilaritySearch(context.Background(), query, 1)
			Expect(err).ToNot(HaveOccurred())

			Expect(len(docs)).To(Equal(1))
			Expect(docs[0].PageContent).To(Equal(expected))
			for k, v := range expectedMeta {
				Expect(docs[0].Metadata[k]).To(Equal(v))
			}
			Expect(docs[0].Score).To(BeNumerically(scoreComparator, scoreVal))
		},
		Entry("nearby", "bandana", bananaDoc.PageContent, bananaDoc.Metadata, ">", float32(0.0)),
		Entry("nearby2", "monkeyish", monkeyDoc.PageContent, monkeyDoc.Metadata, ">", float32(0.0)),
		Entry("exact", "banana", bananaDoc.PageContent, bananaDoc.Metadata, "==", float32(0.0)),
		Entry("exact with metadata", "monkey", monkeyDoc.PageContent, monkeyDoc.Metadata, "==", float32(0.0)),
	)
})

